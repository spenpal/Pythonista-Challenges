def crack(pin: str) -> list[str]:
    return (
        [""]
        if not pin
        else [
            d + pp
            for pp in crack(pin[1:])
            for d in [
                ["0", "8"],
                ["1", "2", "4"],
                ["1", "2", "3", "5"],
                ["2", "3", "6"],
                ["1", "4", "5", "7"],
                ["2", "4", "5", "6", "8"],
                ["3", "5", "6", "9"],
                ["4", "7", "8"],
                ["0", "5", "7", "8", "9"],
                ["6", "8", "9"],
            ][int(pin[0])]
        ]
    )
